"""
Exemplo 2 - Variáveis e Tipos
Este programa mostra como declarar variáveis em Python e seus tipos básicos.
"""
# cria uma variável do tipo string (texto)
nome = "Maria"
# cria uma variável do tipo inteiro (número sem casas decimais)
idade = 20
# cria uma variável do tipo float (número com casas decimais)
altura = 1.65
# cria uma variável do tipo booleano (verdadeiro ou falso)
estudante = True

# imprime o conteúdo das variáveis na tela
print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)
print("É estudante?", estudante)
