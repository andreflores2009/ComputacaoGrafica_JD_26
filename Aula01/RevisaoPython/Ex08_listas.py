"""
Exemplo 8 - Listas
Mostra como criar e manipular listas em Python.
"""
# cria uma lista com três frutas
frutas = ["maçã", "banana", "laranja"]

# imprime a lista inteira
print("Lista de frutas:", frutas)

# adiciona uma fruta à lista
frutas.append("uva")
print("Após adicionar uva:", frutas)

# remove uma fruta da lista
frutas.remove("banana")
print("Após remover banana:", frutas)

# imprime a primeira fruta (índice 0)
print("Primeira fruta:", frutas[0])
