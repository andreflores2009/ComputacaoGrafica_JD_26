"""
Exemplo 9 - Funções
Mostra como criar e chamar funções em Python.
"""
# define uma função que recebe dois parâmetros e retorna a soma
def soma(a, b):
    return a + b

# chama a função passando 5 e 3 como parâmetros
resultado = soma(5, 3)
# imprime o resultado retornado pela função
print("Resultado da soma:", resultado)
