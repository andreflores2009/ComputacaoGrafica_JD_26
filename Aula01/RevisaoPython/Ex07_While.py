"""
Exemplo 7 - Laço de Repetição (while)
Usa o laço while para repetir um bloco de código enquanto a condição for verdadeira.
"""
# inicializa a variável contador com 1
contador = 1

# enquanto o contador for menor ou igual a 5
while contador <= 5:
    # imprime o valor do contador
    print("Contador:", contador)
    # incrementa o contador em +1
    contador += 1
