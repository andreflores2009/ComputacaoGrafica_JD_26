"""
Exemplo 4 - Operações Matemáticas
Demonstra operações matemáticas básicas com duas variáveis.
"""
# define a variável 'a' como 10
a = 10
# define a variável 'b' como 3
b = 3

# imprime a soma
print("Soma:", a + b)
# imprime a subtração
print("Subtração:", a - b)
# imprime a multiplicação
print("Multiplicação:", a * b)
# imprime a divisão normal (float)
print("Divisão:", a / b)
# imprime a divisão inteira (sem decimais)
print("Divisão inteira:", a // b)
# imprime o resto da divisão
print("Resto da divisão:", a % b)
# imprime a potência (a elevado a b)
print("Potência:", a ** b)
